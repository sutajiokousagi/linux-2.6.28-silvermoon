// LCD
// This block (56-59) controls drive strength for the entire block
// of fast IO pins (56-85). See table 108 - basically ZR[0] and ZR[1]
// select drive strength 01X - 04X as follows, with ZR[2] ignored:
// ZR[0]	ZR[1]	ZR[2]	Drive strength
//	0		0		X		01X
//	0		1		X		02X
//	1		0		X		03X
//	1		1		X		04X
// ZR[0] = 57[10] & 59[10] (both have DS02X or DS04X for 1, DS01X or DS03X for 0)
// ZR[1] = 56[11] & 58[11] (both have DS03X or DS04X for 1, DS01X or DS02X for 0)
// ZR[2] = 56[10] & 58[10] (this seems to make no difference according to the docs, but would be same as ZR[0])
// Set drive strength for pins 56-85 to 02X
// ZR[0]==0 and ZR[1]==1 means DS03X for all
